            <p>Dental bridges replace missing teeth. Specifically, they can replace one tooth or a row of missing teeth.</p>
            <p>As the name implies, this appliance literally “bridges the gap” in your smile. Dentists can create custom
                bridges that match the shade of your natural teeth.</p>

            <h2>There are different parts that make up a dental bridge:</h2>
            <ul>
                <li>Abutments are the structures that support your dental bridge. While the term often refers to natural teeth,
                    it can also refer to tiny connector posts used in dental implant-supported bridges.</li>
                <li>Pontics are the artificial (false) teeth that fill in the gap left behind by missing teeth.
                    Depending on your situation and the type of bridge you receive, your bridge might consist of one or more abutments and one or more pontics.</li>
            </ul>

            <h2>Types of dental bridges</h2>
            <p>Dentists use different types of bridges based on your oral health goals. The main four types of dental bridges include:</p>

            <ul>
                <li>Traditional dental bridge.</li>
                <p>This is the most common type of dental bridge. It consists of dental crowns (caps) on both ends with pontics (artificial teeth) in between.
                    A dentist bonds the crowns to your natural teeth (abutments) on either side of the gap, and the artificial teeth (pontics) fill in the space
                    between. Dentists use traditional dental bridges when you have healthy natural teeth on both sides of the gap.</p>
                <li>Cantilever dental bridge.</li>
                <p> A cantilever bridge is similar to a traditional bridge. But there’s only a crown on one end, not both. So, when your dentist bonds the bridge
                    onto your abutment tooth, the artificial tooth (pontic) “hangs over” or extends across the gap. Dentists use cantilever bridges when you only
                    have natural teeth on one side of the gap. Due to their design, cantilever bridges aren’t as strong as traditional bridges.</p>
                <li>Maryland dental bridge.</li>
                <p>A Maryland bridge (or resin-bonded bridge) uses metal wings instead of crowns to secure your bridge. A dentist bonds the wings to the backs
                    of your neighboring teeth to hold your bridge in place. Dentists typically use Maryland bridges to replace front teeth. These appliances aren’t
                    strong enough to withstand the chewing forces of back teeth.</p>
                <li>Implant-supported bridge.</li>
                <p> An implant-supported bridge is similar to a traditional bridge, but it rests atop dental implants instead of natural teeth. Dental implants
                    are small threaded posts that replace missing teeth roots. Before attaching a bridge to dental implants, your implants must fully integrate
                    (fuse) with your jawbone. This process takes three to six months on average, but it can take longer depending on your situation.
                    Dentists can use implant-supported bridges when you have three or more missing teeth in a row.</p>
            </ul>

            <p>To determine what type of bridge you need, a dentist will consider several factors, including:</p>

            <ul>
                <li>Your age. (Dentists typically don’t place bridges in children.)</li>
                <li>How many teeth you’re missing.</li>
                <li>The size of the gap.</li>
                <li>The condition of your teeth next to the gap.</li>
                <li>Whether you have natural teeth on both sides of the gap.</li>
                <li>The overall health of your teeth and gums.</li>
                <li>Your personal preferences.</li>
            </ul>

            <h2>PROCEDURE DETAILS</h2>
            <h3>Traditional or cantilever bridge</h3>
            <p>To place a traditional or cantilever bridge, your dentist will:</p>
            <ul>
                <li>Give you local anesthesia to keep you comfortable during your dental bridge procedure.
                    Reshape your abutment teeth (the natural teeth that’ll support your new bridge). To do this, they’ll need to remove some tooth enamel.
                    This step is irreversible.</li>
                <li>Take dental impressions and send them to a dental laboratory. There, a lab technician will use them to create your final bridge.</li>
                <li>Place a temporary bridge until a lab creates your final bridge. It usually takes about two to four weeks to make a dental bridge.</li>
            </ul>

            <p>During a second office visit (once your final bridge is ready), your dentist will:</p>
            <ul>
                <li>Remove your temporary dental bridge.</li>
                <li>Try on your new final bridge and check the fit.</li>
                <li>Bond (cement) your new dental bridge in place.</li>
                <li>Some dentists use CAD/CAM (computer-aided design and computer-aided manufacturing) technology to create custom bridges in their
                    office during the same appointment. Ask your dentist if this is a possibility for you.</li>
            </ul>

            <h3>Maryland bridge</h3>
            <p>To place a Maryland bridge, your dentist will:</p>
            <ul>
                <li>Prepare your teeth for the metal wings.</li>
                <li>Take dental impressions and send them to a laboratory. (A lab technician will use these to make your final dental bridge.)</li>
            </ul>

            <p>Once your final bridge is ready, your dentist will schedule a second office visit. During this appointment, they’ll:</p>
            <ul>
                <li>Try in your new Maryland bridge and check the fit.</li>
                <li>Apply dental etch to the back surfaces of your neighboring (abutment) teeth. This allows for bonding of the dental cement.</li>
                <li>Bond the wings of the Maryland bridge to the backs of your abutment teeth using dental resin cement.</li>
            </ul>

            <h3>Implant-supported bridge</h3>
            <p>An implant-supported bridge requires several office visits, including one surgery appointment.</p>
            <p>During the first appointment, a dentist, periodontist or oral surgeon will:</p>

            <ul>
                <li>Give you anesthesia to numb your mouth and keep you comfortable.</li>
                <li>Place dental implants into your jaw during an oral surgery procedure.</li>
                <li>After surgery, your implants will need to heal and fuse to your jawbone (a process known as osseointegration).
                    This process takes three to six months on average, but it could take longer depending on your situation.</li>
            </ul>

            <p>Once your dental implants have integrated (fused), your dentist will:</p>
            <ul>
                <li>Attach impression copings to your dental implants and take dental impressions.
                    (Impression copings are tiny connector posts that extend slightly beyond your gum line.)</li>
                <li>Take dental impressions with the copings in place. They’ll send the impressions to a dental lab.</li>
                <li>Remove the abutments while you wait for the lab to make your new implant-supported bridge.</li>
            </ul>

            <p>Once your final implant-supported bridge is ready, your dentist will:</p>
            <ul>
                <li>Place the implant abutments and attached bridge to your dental implants and confirm the fit.</li>
                <li>Secure your bridge in place. Your dentist may use dental cement or tiny screws to do this. (It shouldn’t hurt, though.)</li>
                <li>Because dental implants take a few months to integrate (fuse) with your jaw, implant-supported bridges take longer than other types of bridges.</li>
            </ul>

